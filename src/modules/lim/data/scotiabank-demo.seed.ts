export function enrichLimAuditLog(auditLog: PricingAuditEntry[]): PricingAuditEntry[] {
  const merged = [...auditLog];
  for (const entry of LIM_AUDIT_BASELINE) {
    if (!merged.some((item) => item.id === entry.id)) {
      merged.push(entry);
    }
  }
  return merged
    .sort((a, b) => +new Date(b.timestamp) - +new Date(a.timestamp))
    .slice(0, 100);
}

